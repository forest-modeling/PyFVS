      SUBROUTINE LLLIST (IP)
      IMPLICIT NONE
C----------
C  **LLLIST  DATE OF LAST REVISION:  07/31/08
C----------
C
C     PRINT A LINKED LIST.
C
C     PART OF THE PARALLEL PROCESSING EXTENSION TO PROGNOSIS.
C     P.W. THOMAS--FORESTRY SCIENCES LAB, MOSCOW, ID--MARCH 1986
C
C     PARAMETERS :
C
C      IP - POINTER TO LINKED LIST TO BE PRINTED.
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'PPEPRM.F77'
C
C
      INCLUDE 'PPLLCM.F77'
C
C
COMMONS
C
      INTEGER IP,IQ
C
      IQ=IP
   20 CONTINUE
      IF (IQ.EQ.INULL) GOTO 30
      WRITE (6,100) IELEM (IQ)
  100 FORMAT (1X,I7)
      IQ=LKNEX(IQ)
      GOTO 20
   30 CONTINUE
      RETURN
      END
