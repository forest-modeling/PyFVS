      SUBROUTINE SPASGN (IREC)
      IMPLICIT NONE
C----------
C  **SPASGN  DATE OF LAST REVISION:  07/31/08
C----------
C
C     ALLOCATE ONE RECORD OF PERMANENT SPACE FROM DIRECT ACCESS
C     FILE IDAIO.
C
C     PART OF THE PARALLEL PROCESSING EXTENSION OF PROGNOSIS SYSTEM.
C     P.W. THOMAS--FORESTRY SCIENCES LAB, MOSCOW, ID--JULY 1982
C
C     CALLED FROM PUTSTD AND STASH.
C
C       IREC = LOCATION OF NEXT PERMANENT RECORD.
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'PPEPRM.F77'
C
C
      INCLUDE 'PPCNTL.F77'
C
C
COMMONS
C
      INTEGER IREC
C
C     IF THERE ARE NO AVAILABLE RECORDS, HALT EXECUTION OF PPE.
C
      IF (IAVAIL.EQ.0) CALL PPERRP (.FALSE.,1)
C
C     GET NEXT RECORD, AND SET THE RECORD POINTER TO ZERO, INDICATING
C     THAT THE RECORD IS THE LAST ONE IN A LIST.
C
C
      IREC=IAVAIL
      IAVAIL=INEXR(IREC)
      INEXR(IREC)=0
      RETURN
      END
