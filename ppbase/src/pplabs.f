      SUBROUTINE PPLABS (JPRT)
      IMPLICIT NONE
C----------
C  **PPLABS  DATE OF LAST REVISION:  07/31/08              
C----------
C
C     PRINTS THE LABEL INFO FOR STAND IOUTST TO FILE JPRT.
C
C     PART OF THE PARALLEL PROCESSING EXTENSION OF PROGNOSIS SYSTEM.
C     N.L. CROOKSTON--FORESTRY SCIENCES LAB, MOSCOW, ID--OCT 1987
C
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'PPEPRM.F77'
C
C
      INCLUDE 'PPCNTL.F77'
C
C
      INCLUDE 'OPCOM.F77'
C
C
COMMONS
C
      INTEGER JPRT
      CHARACTER*11 CISN2
C
C     WRITE THE BRANCHING INFO.
C
      CALL PPISN  (CISN2)
      CALL PRTBRH (JPRT,CISN2)
C
C     WRITE THE MULTISTAND POLICY LABELS.
C
      CALL HVLBWT (JPRT)
C
C     WRITE THE STAND POLICY LABELS.
C
      IF (LNSETS(ISTND).GT.0) THEN
         LENSLS=LNSETS(ISTND)
         SLSET =SLSETS(ISTND)
         LBSETS=.TRUE.
         CALL LBSPLW (JPRT)
      ENDIF
      RETURN
      END
