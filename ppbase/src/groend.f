      SUBROUTINE GROEND
      IMPLICIT NONE
C----------
C  **GROEND  DATE OF LAST REVISION:  07/31/08
C----------
C
C     COMPLETES THE PROJECTION FOR ONE STAND.
C
C     PART OF THE PARALLEL PROCESSING EXTENSION OF PROGNOSIS SYSTEM.
C     N.L. CROOKSTON--FORESTRY SCIENCES LAB, MOSCOW, ID--SEPT 1979
C
C     CALLED FROM: PPMAIN
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'PPEPRM.F77'
C
C
      INCLUDE 'CONTRL.F77'
C
C
      INCLUDE 'PLOT.F77'
C
C
      INCLUDE 'ECON.F77'
C
C
      INCLUDE 'OUTCOM.F77'
C
C
      INCLUDE 'PPCNTL.F77'
C
C
COMMONS
C
C
      ICL6=-99
      ONTREM(7)=0.0
      OLDTPA=TPROB
      OLDBA=BA
      OLDAVH=AVH
      ORMSQD=RMSQD
      RELDM1=RELDEN
      CALL LDSUM (3)
      CALL STOPRT
C
C     CALL **ECEND** TO OUTPUT LAST LINES OF ECONOMIC OUTPUT
C     AND CALL ECLBL TO OUTPUT ECONOMIC LABEL INFORMATION.
C
      IF (LECON) THEN
         CALL ECEND
         CALL ECLBL
      ENDIF
C
C     STORE THE POST-PROJECTION BASE MODEL DATA.
C
      CALL SPARLS (ISNKEY(ISTND,2),ISNKEY(ISTND,3))
      CALL PUTSTD
C
C     IF ANY EXTENSIONS ARE ACTIVE AND IF THE KEYWORD TABLE IS OPEN,
C     THEN: CLOSE THE TABLE.
C
      IF (.NOT.LFLAG) THEN
         WRITE (JOPPRT,10)
   10    FORMAT (/1X,130('-'))
         LFLAG=.TRUE.
      ENDIF
      RETURN
      END
