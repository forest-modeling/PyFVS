      SUBROUTINE GPADD (KODE,IDT,IACTK,NPRMS,PRMS,NSTND,ISTDLS)
      IMPLICIT NONE
C----------
C  **GPADD   DATE OF LAST REVISION:  07/31/08
C----------
C
C     ADD A GLOBAL ACTIVITY AFTER PROJECTION HAS STARTED.
C
C     PART OF THE PARALLEL PROCESSING EXTENSION TO PROGNOSIS.
C     P.W. THOMAS--FORESTRY SCIENCES LAB, MOSCOW, ID--MARCH 1986
C
C     KODE  = RETURN CODE, 0 IF NOTHING WENT WRONG.
C     IDT   = DATE FOR NEW OPTION.
C     IACTK = GLOBAL ACTIVITY NUMBER.
C     NPRMS = NUMBER OF PARAMETERS.
C     PRMS  = ARRAY OF PARAMETERS.
C     NSTND = NUMBER OF STANDS THIS OPTION APPLIES TO.
C     ISTDLS= ARRAY OF STANDS THIS OPTION APPLIES TO.
C
COMMONS
C
C
      INCLUDE 'PPGPCM.F77'
C
C
COMMONS
C
C
      INTEGER IACTK,IDT,KODE,NPRMS,NSTND,I
      REAL PRMS(NPRMS)
      INTEGER ISTDLS(NSTND)
      IF (NSTND.LE.0) GOTO 100
      CALL GPNEW (KODE,IDT,IACTK,NPRMS,PRMS)
      IF (KODE.GT.0) GOTO 100
      DO 40 I=1,NSTND
      CALL LLADD (ISTDLS(I),MIACT(4,MIMGL),MIACT(5,MIMGL))
   40 CONTINUE
  100 CONTINUE
      RETURN
      END
