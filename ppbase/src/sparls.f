      SUBROUTINE SPARLS (IBEGIN,IEND)
      IMPLICIT NONE
C----------
C  **SPARLS  DATE OF LAST REVISION:  07/31/08
C----------
C
C     RELEASE AN ALLOCATED LIST BACK TO FREE SPACE.
C
C     PART OF THE PARALLEL PROCESSING EXTENSION OF PROGNOSIS SYSTEM.
C
C     IBEGIN= STARTING RECORD OF LIST.
C     IEND  = ENDING RECORD OF LIST.
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'PPEPRM.F77'
C
C
      INCLUDE 'PPCNTL.F77'
C
C
COMMONS
C
      INTEGER IEND,IBEGIN
C
C
      IF (PDEBUG) WRITE (JOPPRT,10) IBEGIN, IEND
   10 FORMAT (' IN SPARLS: IBEGIN, IEND= ',2I10)
C
      IF (IBEGIN .EQ. 0 .OR. IEND .EQ. 0) RETURN
      INEXR (IEND) = IAVAIL
      IAVAIL = IBEGIN
      RETURN
      END
