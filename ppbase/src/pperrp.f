      SUBROUTINE PPERRP ( LRETRN,IERRN )
      IMPLICIT NONE
C----------
C  **PPERRP DATE OF LAST REVISION:  07/31/08
C----------
C
C     ERROR PROCESSOR FOR PARALLEL PROCESSING EXTENSION.
C
      LOGICAL LRETRN
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'CONTRL.F77'
C
C
      INCLUDE 'PLOT.F77'
C
C
COMMONS
C
      INTEGER IERRN
      CHARACTER*4 PREF(3)
      DATA PREF/' ***','****','*   '/
      GO TO( 1100,1200,1300,1400,1500,1600,1700,1800,1900,2000
     >      ,2100,2200,2300,2400,2500,2600,2700,2800,2900,3000
     >      ),  IERRN
 1100 CONTINUE
C                        ERROR NUMBER 1
      WRITE (JOSTND,1110) PREF
 1110 FORMAT (/3A4,'PPE01 ERROR:  SPACE ON DIRECT ACCESS FILE IS',
     >        ' DEPLETED.')
      IF (ICCODE .LT. 4) ICCODE=4
      GO TO 9000
 1200 CONTINUE
C                        ERROR NUMBER 2
      WRITE (JOSTND,1210) PREF
 1210 FORMAT (/3A4,'PPE02 ERROR:  GLOBAL ACTIVITY STORAGE IS FULL.',
     >        '  OPTION IS IGNORED.')
      IF (ICCODE .LT. 3) ICCODE=3
      GO TO 9000
 1300 CONTINUE
C                        ERROR NUMBER 3
      WRITE (JOSTND,1310) PREF
 1310 FORMAT (/3A4,'PPE03 ERROR:  GLOBAL OPTIONS ONLY USE REAL YEARS.')
      IF (ICCODE .LT. 3) ICCODE=3
      GO TO 9000
 1400 CONTINUE
C                        ERROR NUMBER 4
      WRITE (JOSTND,1410) PREF
 1410 FORMAT (/3A4,'PPE04 ERROR:  MAXIMUM NUMBER OF STANDS HAS BEEN',
     >        ' EXCEEDED.')
      IF (ICCODE .LT. 3) ICCODE=3
      GO TO 9000
 1500 CONTINUE
C                        ERROR NUMBER 5
      WRITE (JOSTND,1510) PREF
 1510 FORMAT (/3A4,'PPE05 ERROR:  INVENTORY YEAR FOR CURRENT STAND',
     >        ' IS PAST THE MASTER STARTING YEAR.')
      IF (ICCODE .LT. 3) ICCODE=3
      GO TO 9000
 1600 CONTINUE
C                        ERROR NUMBER 6
      WRITE (JOSTND,1610) PREF
 1610 FORMAT(/3A4,'PPE06 ERROR:  MAXIMUM NUMBER OF CYCLES EXCEEDED.')
      IF (ICCODE .LT. 3) ICCODE=3
      GO TO 9000
 1700 CONTINUE
C                        ERROR NUMBER 7
      WRITE (JOSTND,1710) PREF
 1710 FORMAT (/3A4,'PPE07 ERROR:  KEYWORD IS ILLEGAL WHEN IT ',
     >        'FOLLOWS RESTART.')
      IF (ICCODE.LT.3) ICCODE=3
      GO TO 9000
 1800 CONTINUE
C                        ERROR NUMBER 8
      WRITE (JOSTND,1810) PREF
 1810 FORMAT (/3A4,'PPE08 ERROR:  NO STANDS OR DATA TO PROCESS.')
      IF (ICCODE .LT. 2) ICCODE=2
      GO TO 9000
 1900 CONTINUE
C                        ERROR NUMBER 9
      WRITE (JOSTND,1910) PREF
 1910 FORMAT(/3A4,'PPE09 ERROR:  ILLEGAL ATTEMPT TO REDEFINE THE',
     >        ' MASTER TIME INTERVALS, STARTING YEAR, OR NUMBER',
     >        ' OF CYCLES.')
      IF (ICCODE .LT. 3) ICCODE=3
      GO TO 9000
 2000 CONTINUE
C                        ERROR NUMBER 10
      WRITE (JOSTND,2010) PREF
 2010 FORMAT(/3A4,'PPE10 ERROR:  MAXIMUM NUMBER OF: 1) STANDS*CYCLES,',
     >       ' OR 2) UNIQUE INTERNAL STAND NUMBERS HAS BEEN EXCEEDED;'/
     >       T27,'STAND TABLES CAN NOT BE PROCESSED.')
      IF (ICCODE .LT. 3) ICCODE=3
      GO TO 9000
 2100 CONTINUE
C                        ERROR NUMBER 11
      WRITE (JOSTND,2110) PREF
 2110 FORMAT (/3A4,'PPE11 ERROR:  TOO MANY YEARS/HARVESTS; ',
     >        'COMPOSITE YIELD TABLE CAN NOT BE COMPUTED.')
      IF (ICCODE .LT. 3) ICCODE=3
      GO TO 9000
 2200 CONTINUE
C                        ERROR NUMBER 12
      WRITE (JOSTND,2210) PREF
 2210 FORMAT (/3A4,'PPE12 ERROR:  PRINT-DATA ELEMENT DESCRIPTION ',
     >        'WORD DOES NOT MATCH VALUE FOUND IN THE DATA BASE.')
      IF (ICCODE .LT. 4) ICCODE=4
      GOTO 9000
 2300 CONTINUE
C                        ERROR NUMBER 13
      WRITE (JOSTND,2310) PREF
 2310 FORMAT (/3A4,'PPE13 ERROR:  A BRANCH CAN NOT BE TRACED BACK TO ',
     >        'THE BEGINNING OF THE PROJECTION.')
      IF (ICCODE .LT. 4) ICCODE=4
      GOTO 9000
 2400 CONTINUE
C                        ERROR NUMBER 14
      WRITE (JOSTND,2410) PREF
 2410 FORMAT (/3A4,'PPE14 ERROR:  MAXIMUM BRANCH NODES EXCEEDED.')
      IF (ICCODE .LT. 3) ICCODE=3
      GOTO 9000
 2500 CONTINUE
C                        ERROR NUMBER 15
      WRITE (JOSTND,2510) PREF
 2510 FORMAT (/3A4,'PPE15 ERROR:  ILLEGAL ATTEMPT TO CREATE A BRANCH ',
     >     'BEFORE FIRST MASTER CYCLE; FIRST ALTERNATIVE IS SCHEDULED.')
      IF (ICCODE .LT. 3) ICCODE=3
      GOTO 9000
 2600 CONTINUE
C                        ERROR NUMBER 16
      WRITE (JOSTND,2610) PREF
 2610 FORMAT (/3A4,'PPE16 ERROR:  TOO MANY MULTISTAND POLICIES.  ',
     >   'KEYWORD IGNORED.')
      IF (ICCODE.LT.2) ICCODE=2
      GOTO 9000
 2700 CONTINUE
C                        ERROR NUMBER 17
      GO TO 9000
 2800 CONTINUE
C                        ERROR NUMBER 18
      GO TO 9000
 2900 CONTINUE
C                        ERROR NUMBER 19
      GO TO 9000
 3000 CONTINUE
C                        ERROR NUMBER 20
 9000 CONTINUE
C
C
      IF (LRETRN) RETURN
C
C
      CALL GRSTOP
      END
