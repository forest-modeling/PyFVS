      SUBROUTINE LLNEW (IP)
      IMPLICIT NONE
C----------
C  **LLNEW   DATE OF LAST REVISION:  07/31/08
C----------
C
C     GET A NEW LINKED LIST ELEMENT.
C
C     PART OF THE PARALLEL PROCESSING EXTENSION TO PROGNOSIS.
C     P.W. THOMAS--FORESTRY SCIENCES LAB, MOSCOW, ID--MARCH 1986
C
C     PARAMETERS :
C
C       IP - POINTER TO NEW LINKED LIST ELEMENT.
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'PPEPRM.F77'
C
C
      INCLUDE 'PPLLCM.F77'
C
C
      INCLUDE 'PPCNTL.F77'
C
C
COMMONS
C
      INTEGER IP
C
      IP=IFRBEG
      IFRBEG=LKNEX(IP)
      IF (IFRBEG.EQ.INULL) CALL LLERRP (1,JOPPRT,.FALSE.)
      LKNEX(IP)=INULL
      RETURN
      END
