      SUBROUTINE LLCONC (IBEG1,IEND1,IBEG2,IEND2)
      IMPLICIT NONE
C----------
C  **LLCONC  DATE OF LAST REVISION:  07/31/08
C----------
C
C     CONCATENATE TWO INTEGER LINKED LISTS TOGETHER.
C
C     PART OF THE PARALLEL PROCESSING EXTENSION TO PROGNOSIS.
C     P.W. THOMAS--FORESTRY SCIENCES LAB, MOSCOW, ID--MARCH 1986
C
C     PARAMETERS :
C
C       IBEG1 - POINTER TO BEGINNING OF FIRST LINKED LIST.
C       IEND1 - POINTER TO END OF FIRST LINKED LIST.
C       IBEG2 - POINTER TO BEGINNING OF SECOND LINKED LIST.
C       IEND2 - POINTER TO END OF SECOND LINKED LIST.
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'PPEPRM.F77'
C
C
      INCLUDE 'PPLLCM.F77'
C
C
COMMONS
C
C
      INTEGER IEND2,IBEG2,IEND1,IBEG1,IT
C
C
      IT=IEND1
      LKNEX (IT) = IBEG2
      IEND1=IEND2
      IBEG2=INULL
      IEND2=INULL
      RETURN
      END
