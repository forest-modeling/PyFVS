      SUBROUTINE GRCEND
      IMPLICIT NONE
C----------
C  **GRCEND--PPBASE  DATE OF LAST REVISION:  07/31/08
C----------
C
C     COMPUTE SOME END-OF-CYCLE STATISTICS AND STORE THEM.
C
C     PART OF THE PARALLEL PROCESSING EXTENSION OF PROGNOSIS SYSTEM.
C     N.L. CROOKSTON--FORESTRY SCIENCES LAB, MOSCOW, ID--MAY 1982.
C
C     CALLED FROM: PPMAIN
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'PPEPRM.F77'
C
C
      INCLUDE 'PPCNTL.F77'
C
C
      INCLUDE 'CONTRL.F77'
C
C
COMMONS
C
C     ASSIGN THE EXAMPLE TREES TO THE OUTPUT ARRAYS.
C
      IF (PDEBUG) WRITE (JOPPRT,60) ICYC,CISNUM(ISTND)
   60 FORMAT (/,' IN GRCEND, CALLING LDSUM & STOPRT, CYCLE = ',I4,
     >        '; CISNUM=',A10)
C
      CALL EXTREE
C
C     STORE SUMMARY DATA
C
      CALL LDSUM (2)
C
C     WRITE STAND STATISTICS ON PRINT-DATA DA FILE
C
      CALL STOPRT
C
C     CALL RESAGE TO RESET STAND AGE.
C
      CALL RESAGE
C
C     DWARF MISTLETOE MODEL OUTPUT
C
      CALL MISPRT
C
C     WESTERN ROOT DISEASE MODEL OUTPUT
C
      CALL RDPR
C
C     BLISTER RUST MODEL OUTPUT
C
      CALL BRPR
C
C     PRINT TREELIST, IF DESIRED.
C
      CALL PRTRLS (1)
C
C     IF RUNNING FVSSTAND POST-PROCESSOR, CALL FILE PRINTER
C
      CALL FVSSTD (1)
C
C     IF NAT CRUISE OUTPUT IS REQUESTED ... CALL NATCRZ PRINTER.
C
      CALL NATCRZ (1)
C
C     MAKE SURE THAT THE VALUE OF SELECTION IS SET UNDEFINED
C     FOR THE STAND.
C
      IF (LHVALL) CALL EVUST4(9)
C
C     RESET THE PROCESSING PHASE FOR THIS STAND.
C
      ISNKEY(ISTND,4)=0
      ICYC=ICYC+1
      RETURN
      END
