      SUBROUTINE LLERRP (IERRN,JOUT,LRETRN)
      IMPLICIT NONE
C----------
C  **LLERRP  DATE OF LAST REVISION:  07/31/08
C----------
C
C     ERROR MESSAGE ROUTINE FOR LINKED LISTS.
C
C     PART OF THE PARALLEL PROCESSING EXTENSION TO PROGNOSIS.
C     P.W. THOMAS--FORESTRY SCIENCES LAB, MOSCOW, ID--MARCH 1986
C
C     PARAMETERS :
C
C       IERRN - ERROR NUMBER.
C       JOUT  - LOGICAL UNIT NUMBER FOR OUTPUT.
C       LRETRN - TRUE IF RETURNING.
C
      INTEGER JOUT,IERRN
      LOGICAL LRETRN
      WRITE (JOUT,100)
  100 FORMAT (/' ERROR: YOU RAN OUT OF FREE SPACE IN A LINKED LIST.')
      CALL RCDSET (5,LRETRN)
      RETURN
      END
