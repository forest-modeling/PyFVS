      SUBROUTINE LLCOPY (IARRAY,IMAX,IC,IBEG)
      IMPLICIT NONE
C----------
C  **LLCOPY  DATE OF LAST REVISION:  07/31/08
C----------
C
C     COPY A LINKED LIST INTO AN ARRAY.
C
C     PART OF THE PARALLEL PROCESSING EXTENSION TO PROGNOSIS.
C     P.W. THOMAS--FORESTRY SCIENCES LAB, MOSCOW, ID--MARCH 1986
C
C     IARRAY= DESTINATION ARRAY FOR LINKED LIST.
C     IMAX  = MAXIMUM LENGTH OF IARRAY.
C     IC    = NUMBER OF LINKED LIST ELEMENTS RETURNED.
C     IBEG  = POINTER TO BEGINNING OF LINKED LIST.
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'PPEPRM.F77'
C
C
      INCLUDE 'PPLLCM.F77'
C
C
COMMONS
C
      INTEGER IARRAY(IMAX),IBEG,IC,IMAX,IP
      IC=0
      IP=IBEG
   35 CONTINUE
      IC=IC+1
      IF (IP.EQ.INULL) GOTO 40
      IF (IC.GT.IMAX) GOTO 40
      IARRAY(IC)=IELEM(IP)
      IP=LKNEX(IP)
      GOTO 35
   40 CONTINUE
      IC=IC-1
      RETURN
      END
