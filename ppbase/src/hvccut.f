      SUBROUTINE HVCCUT (IMCCUT)
      IMPLICIT NONE
C----------
C  **HVCCUT DATE OF LAST REVISION:  07/31/08              
C----------
C     RETURN IMCCUT FOR CURRENT STAND.
C
C     IMCCUT= -1 IF STAND MEETS THE DEF. OF A CLEAR CUT STAND.
C             +1 IF STAND DOES NOT MEET THE DEF. OF A CCUT STAND.
C
C     MULTISTAND POLICY ROUTINE - N.L. CROOKSTON  - JULY 1987
C     FORESTRY SCIENCES LABORATORY - MOSCOW, ID 83843
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'PPEPRM.F77'
C
C
      INCLUDE 'OPCOM.F77'
C
C
      INCLUDE 'PPCNTL.F77'
C
C
      INCLUDE 'PPHVCM.F77'
C
C
C
COMMONS
C
C
      INTEGER IMCCUT
C
C     DECIDE IF THE CURRENT STAND IS CLEAR CUT.
C
      IMCCUT=1
      IF (TSTV2(1).LT.DCTPA .AND. TSTV2(7).LT.DCTOP) IMCCUT=-1
C
      IF (LHVDEB) WRITE (JOPPRT,'('' IN HVCCUT, TSTV2(1&7)='',2F10.3,
     >  '' DCTPA,DCTOP='',2F10.3,'' IMCCUT='',I2)') TSTV2(1),TSTV2(7),
     >  DCTPA,DCTOP,IMCCUT
      RETURN
      END
