      SUBROUTINE LLSCRH (IBEG,IEND)
      IMPLICIT NONE
C----------
C  **LLSCRH  DATE OF LAST REVISION:  07/31/08
C----------
C
C     SCRATCH AN ENTIRE LINKED LIST BY CONCATENATING IT
C     WITH FREESPACE.
C
C     PART OF THE PARALLEL PROCESSING EXTENSION TO PROGNOSIS.
C     P.W. THOMAS--FORESTRY SCIENCES LAB, MOSCOW, ID--MARCH 1986
C
C     SUBROUTINES CALLED :
C
C       LLCONC - CONCATENATE TWO LINKED LISTS TOGETHER.
C
C     PARAMETERS :
C
C       IBEG - POINTER TO BEGINNING OF LINKED LIST.
C       IEND - POINTER TO END OF LINKED LIST.
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'PPEPRM.F77'
C
C
      INCLUDE 'PPLLCM.F77'
C
C
COMMONS
C
      INTEGER IEND,IBEG
C
      CALL LLCONC (IFRBEG,IFREND,IBEG,IEND)
      RETURN
      END
