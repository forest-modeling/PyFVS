      SUBROUTINE LLINIT
      IMPLICIT NONE
C----------
C  **LLINIT  DATE OF LAST REVISION:  07/31/08
C----------
C
C     INITIALIZE FREESPACE.
C
C     PART OF THE PARALLEL PROCESSING EXTENSION TO PROGNOSIS.
C     P.W. THOMAS--FORESTRY SCIENCES LAB, MOSCOW, ID--MARCH 1986
C
C     CALLED FROM :
C
C       GPINIT - INITIALIZE GLOBAL PROCESSOR.
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'PPEPRM.F77'
C
C
      INCLUDE 'PPLLCM.F77'
C
C
COMMONS
C
      INTEGER NMINUS,I
C
      IFRBEG=1
      IFREND=MXLLSZ
      INULL=0
      NMINUS=MXLLSZ-1
      DO 40 I=IFRBEG,NMINUS
      LKNEX(I)=I+1
   40 CONTINUE
      LKNEX(MXLLSZ)=INULL
      RETURN
      END
