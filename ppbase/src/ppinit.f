      SUBROUTINE PPINIT
      IMPLICIT NONE
C----------
C  **PPINIT  DATE OF LAST REVISION:  07/31/08
C----------
C
C     INITIALIZE PARALLEL PROCESSING VARIABLES.
C
C     PART OF THE PARALLEL PROCESSING EXTENSION OF PROGNOSIS SYSTEM.
C     N.L. CROOKSTON--FORESTRY SCIENCES LAB, MOSCOW, ID--DEC 1979
C
C     CALLED FROM PPIN
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'PPEPRM.F77'
C
C
      INCLUDE 'PPCNTL.F77'
C
C
      INCLUDE 'PPDNCM.F77'
C
C
      INCLUDE 'PPEXCM.F77'
C
C
      INCLUDE 'CONTRL.F77'
C
C
COMMONS
C
      INTEGER I
C
      MIY(1)=0
      DO 10 I=1,MAXCYC
      MIY(I+1)=10
      LPAUSE(I)=.FALSE.
   10 CONTINUE
      CAUTID='0000'
      NAUTID=0
      DCTOP=10.0
      DCTPA=60.0
      LAREAS=.FALSE.
      LRESTR=.FALSE.
      IPMODI=1
      NOSTND=0
      LASTIN=.FALSE.
      ISTND=0
      LMINT=.FALSE.
      LMCYC=.FALSE.
      LSTYR=.FALSE.
      JOCOPY=JOSUM
      JOPPRT=JOSTND
      JCPCMP=0
      JCPSUM=0
      JEXOPT=-1
      JLBWTS=JOPPRT
      LBWTS=.FALSE.
      CLBWID=' LBW'
      CMPID=NSET
      MICYC=1
      NAVPRT=0
      LHVALL=.FALSE.
      LNEDNS=.FALSE.
      LEORUN=.FALSE.
      IMSREC=0
      IMSEND=0
      NBYGRP=0
C
      LTMPAC=.FALSE.
      LTMPAD=.FALSE.
C
C     INITIALIZE THE "SELECT" CONDITIONS.
C
      CALL SELINT
C
C     INITIALIZE THE PPE LEVEL OPCODE AND USER DEFINED VARIABLES.
C
      NPTST2=0
      NPTST3=1
      NPEXCD=1
      LEXPON=.FALSE.
C
C     INITIALIZE THE SPATIAL DATA SETS
C
      CALL SPLAIT
      CALL SPNBIT
      CALL SPRDIT
C
C     INITIALIZE THE MULTISTAND POLICY DATA.
C
      CALL HVINIT
C
C     INITIALIZE THE ALLDATA DA SPACE POINTERS AND GLOBAL OPTIONS.
C
      CALL SPAMGM
      CALL GPINIT
      RETURN
      END
