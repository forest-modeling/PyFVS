      SUBROUTINE GPCSET
      IMPLICIT NONE
C----------
C  **GPCSET  DATE OF LAST REVISION:  07/31/08
C----------
C
C     CHANGE MASTER CYCLE NUMBERS TO DATES.
C
C     PART OF THE PARALLEL PROCESSING EXTENSION TO PROGNOSIS.
C     P.W. THOMAS--FORESTRY SCIENCES LAB, MOSCOW, ID--MARCH 1986
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'PPEPRM.F77'
C
C
      INCLUDE 'PPGPCM.F77'
C
C
      INCLUDE 'PPCNTL.F77'
C
C
COMMONS
C
C
C
      INTEGER I
C
C
      IF (MIMGL.LE.0) GOTO 100
      DO 40 I=1,MIMGL
      IF (MIDATE(I).LE.MNCYC) MIDATE(I)=MIY(MIDATE(I))
   40 CONTINUE
  100 CONTINUE
      RETURN
      END
