      SUBROUTINE PPINTR
      IMPLICIT NONE
C----------
C  **PPINTR  DATE OF LAST REVISION:  07/31/08
C----------
C
C     SEE IF MODE 2 ACTIVITIES ARE SCHEDULED, AND MAKE SURE THERE
C     ARE NO MODE 1 CONFLICTS.
C
C     PART OF THE PARALLEL PROCESSING EXTENSION TO PROGNOSIS.
C     P.W. THOMAS--FORESTRY SCIENCES LAB, MOSCOW, ID--MARCH 1986
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'PPEPRM.F77'
C
C
      INCLUDE 'PPCNTL.F77'
C
C
COMMONS
C
C
      INTEGER MYACTS(2),NOVER,I,ISQNUM,KODE,NPRMS,IDT,INUM
      REAL DUMMY(1)
C
C     ACTIVITIES THAT ARE SCHEDULED FOR A STAND THAT PRECLUDE
C     INTERSTAND INTERACTION.
C
      DATA MYACTS/850,851/
      DATA NOVER/2/
      IF (IPMODE(ISTND).EQ.1) GOTO 900
C
C     SEE IF GLOBAL OPTIONS ARE TO BE OVERRIDDEN BY ANY LOCAL OPTIONS.
C
      DO 40 I=1,NOVER
      ISQNUM=0
      CALL OPGET2(MYACTS(I),IDT,MIY(MICYC-1),MIY(MICYC),ISQNUM,1,NPRMS,
     >            DUMMY,KODE)
      IF (KODE.EQ.0) IPMODE(ISTND)=1
      CALL OPEVAC (MYACTS(I),INUM)
      IF (INUM.GT.0) IPMODE(ISTND)=1
   40 CONTINUE
  900 CONTINUE
      IPMODI=IPMODE(ISTND)
      IF (PDEBUG) WRITE (JOPPRT,910) ISTND,IPMODI
  910 FORMAT (' IN PPINTR: ISTND,IPMODI= ',2I5)
      RETURN
      END
