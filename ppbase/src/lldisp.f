      SUBROUTINE LLDISP (IP)
      IMPLICIT NONE
C----------
C  **LLDISP  DATE OF LAST REVISION:  07/31/08
C----------
C
C     DISPOSE OF A LINKED LIST ELEMENT BY REALLOCATING IT TO
C     FREESPACE.
C
C     PART OF THE PARALLEL PROCESSING EXTENSION TO PROGNOSIS.
C     P.W. THOMAS--FORESTRY SCIENCES LAB, MOSCOW, ID--MARCH 1986
C
C     PARAMETERS :
C
C       IP - POINTER TO LINKED LIST ELEMENT TO BE DISPOSED OF.
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'PPEPRM.F77'
C
C
      INCLUDE 'PPLLCM.F77'
C
C
COMMONS
C
      INTEGER IP,IT
C
      IT=IFRBEG
      IFRBEG=IP
      LKNEX(IP)=IT
      RETURN
      END
