      SUBROUTINE SPAMGM
      IMPLICIT NONE
C----------
C  **SPAMGM  DATE OF LAST REVISION:  07/31/08
C----------
C
C     INITIALIZE POINTERS FOR DIRECT ACCESS FILE IDAIO SUCH THAT
C     IDAIO IS EMPTY.
C
C     PART OF THE PARALLEL PROCESSING EXTENSION OF PROGNOSIS SYSTEM.
C     P.W. THOMAS--FORESTRY SCIENCES LAB, MOSCOW, ID--JULY 1982
C
C     CALLED FROM PPINIT AND SPARLS
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'PPEPRM.F77'
C
C
      INCLUDE 'PPCNTL.F77'
C
C
COMMONS
C
      INTEGER NMINUS,I
C
      IAVAIL=IRESRV+1
      NMINUS=NDAREC-1
      DO 40 I=IAVAIL,NMINUS
      INEXR (I)=I+1
   40 CONTINUE
      INEXR (NDAREC)=0
      IENDFR = NDAREC
C
C     ASSIGN THE FIRST WRITE RECORD
C
      CALL SPASGN (IWTREC)
      RETURN
      END
