      SUBROUTINE DSTASH (BUFFER,IPNT)
      IMPLICIT NONE
C----------
C  **DSTASH  DATE OF LAST REVISION:  07/31/08
C----------
C
C     GET A RECORD FROM DIRECT ACCESS FILE IDAIO (DE-STASH IT).
C
C     PART OF THE PARALLEL PROCESSING EXTENSION OF PROGNOSIS SYSTEM.
C
C       BUFFER - ARRAY WHICH WILL CONTAIN INFORMATION READ FROM THE
C                RECORD.
C       IPNT   - POINTER TO LAST WORD IN BUFFER.
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'PPEPRM.F77'
C
C
      INCLUDE 'PPCNTL.F77'
C
C
COMMONS
C
C
      INTEGER IPNT,ISUB,I,I2
      REAL BUFFER (IPNT)
      IF (PDEBUG) WRITE (JOPPRT,20) IRDREC,INEXR(IRDREC),IPNT
   20 FORMAT (' IN DSTASH: IRDREC= ',I5,' INEXR(IRDREC)= ',I5,
     >        ' IPNT= ',I6)
C
C     FIND OUT WHICH ACTUAL RECORD AND FILE WE ARE GOING TO USE.
C
      ISUB=0
      DO 10 I=1,NDAFL
      IF (IRDREC.LE.NDACUM(I)) THEN
         READ (IDAIO(I),REC=IRDREC-ISUB) (BUFFER(I2),I2=1,IPNT)
         IRDREC=INEXR(IRDREC)
         RETURN
      ELSE
         ISUB=NDACUM(I)
      ENDIF
   10 CONTINUE
C
C     THIS LOOP SHOULD NEVER EXIT.
C
      RETURN
      END
