      SUBROUTINE SLCOND
      IMPLICIT NONE
C----------
C  **SLCOND--PPBASE   DATE OF LAST REVISION:  07/31/08
C----------
C
C     CHECKS USER SPECIFIED SLELECT-CONDITIONS  (SELMGMID,...ETC)
C
C     PART OF THE PARALLEL PROCESSING EXTENSION OF PROGNOSIS SYSTEM.
C     N.L. CROOKSTON--FORESTRY SCIENCES LAB, MOSCOW, ID--FEB 1982
C
C     CALLED FROM: SNDTBS AND YIELDS
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'PPEPRM.F77'
C
C
      INCLUDE 'PPCNTL.F77'
C
C
      INCLUDE 'OPCOM.F77'
C
C
      INCLUDE 'PPHVCM.F77'
C
C
COMMONS
C
      INTEGER KSTND,KODE,LEN,I
      LOGICAL LPRINT
      CHARACTER JMGMT*4,ANPLT*26,CISNTS*11
C
      ENTRY SLCND1 (LPRINT,ANPLT,JMGMT)
      LPRINT=.TRUE.
C
C     CHECK THE MANAGEMENT IDENT.
C
      IF (MGM1.EQ.NSET) GOTO 20
      IF (MGM2.EQ.NSET) GOTO 10
      IF (JMGMT.GE.MGM1 .AND. JMGMT.LE.MGM2) GOTO 20
      LPRINT=.FALSE.
      RETURN
   10 CONTINUE
      IF (JMGMT.EQ.MGM1) GOTO 20
      LPRINT=.FALSE.
      RETURN
   20 CONTINUE
C
C     CHECK THE STAND IDENTIFICATION
C
      IF (SID1.EQ.NOTSET) GOTO 40
      IF (SID2.EQ.NOTSET) GOTO 30
      IF (ANPLT.GE.SID1 .AND. ANPLT.LE.SID2) GOTO 40
      LPRINT=.FALSE.
      RETURN
   30 CONTINUE
      IF (ANPLT.EQ.SID1) GOTO 40
      LPRINT=.FALSE.
      RETURN
   40 CONTINUE
      RETURN
C
C
      ENTRY SLCND2 (LPRINT,CISNTS)
C
      LPRINT=.TRUE.
      IF (CSISN.EQ.'-1') RETURN
      IF (CLISN.EQ.'-1') GOTO 50
      IF (CISNTS.GE.CSISN .AND. CISNTS.LE.CLISN) RETURN
   50 CONTINUE
      IF (CISNTS.EQ.CSISN) RETURN
      LPRINT=.FALSE.
      RETURN
C
C
      ENTRY SLCND3 (LPRINT,KSTND)
C
      LPRINT=.TRUE.
      IF (LNSSPL.EQ.0) RETURN
      CALL LBINTR (LNSSPL,SLSPLB,LNSETS(KSTND),SLSETS(KSTND),
     >             LEN,WKSTR1,KODE)
      IF (LEN.GT.0) RETURN
      LPRINT=.FALSE.
      RETURN
C
C
      ENTRY SLCND4 (LPRINT,KSTND)
C
      LPRINT=.TRUE.
      IF (.NOT.LHVALL .OR. LNSHPL.EQ.0) RETURN
      DO 70 I=1,IXHRVP
      IF (IHVSTA(KSTND,I).EQ.0) GOTO 70
      IF (LNSHPL.NE.LNHPLB(I,1)) GOTO 70
      IF (SLHPLB(1:LNSHPL).NE.HVPLAB(I)(1:LNHPLB(I,1))) GOTO 70
      IF (LHIER.AND.I.EQ.1) GOTO 70
      RETURN
   70 CONTINUE
      LPRINT=.FALSE.
      RETURN
      END
