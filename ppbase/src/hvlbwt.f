      SUBROUTINE HVLBWT (JPRT)
      IMPLICIT NONE
C----------
C  **HVLBWT DATE OF LAST REVISION:  07/31/08              
C----------
C
C     WRITE THE MSPLABEL ON DATA SET JPRT
C
C     PART OF THE LABEL PROCESSING COMPONENT OF THE PROGNOSIS SYSTEM
C     N.L. CROOKSTON -- INTERMOUNTAIN RESEARCH STATION -- JAN 1987
C
C     JPRT  = PRINT MESSAGE FILE.
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'PPEPRM.F77'
C
C
      INCLUDE 'PPCNTL.F77'
C
C
      INCLUDE 'PPHVCM.F77'
C
C
COMMONS
C
      INTEGER LSTLAB(15),JPRT,ICNT,ICHARS,IPASS,IHV,I
C
C     DO A LINE FEED.
C
      WRITE (JPRT,'(1X)')
C
C     IF PROCESSING IS NOT ACTIVIATED, BRANCH TO EXIT.
C
      IF (.NOT.LHVALL) GOTO 40
C
C     INITIALIZE SOME COUNTERS.
C
      ICNT=0
      ICHARS=0
      IPASS=0
C
C     DO OVER MULTISTAND POLICIES.
C
      DO 30 IHV=1,IXHRVP
      IF (IHVSTA(ISTND,IHV).EQ.0) GOTO 30
      IF (ICHARS+LNHPLB(IHV,1).LE. 100) THEN
         ICHARS=ICHARS+2+LNHPLB(IHV,1)
         ICNT=ICNT+1
         LSTLAB(ICNT)=IHV
      ELSE
         IPASS=IPASS+1
         IF (IPASS.LE.1) THEN
            WRITE (JPRT,10) (HVPLAB(LSTLAB(I))(1:LNHPLB(LSTLAB(I),1)),
     >                       I=1,ICNT)
   10       FORMAT (' MULTISTAND POLICY LABELS: ',15(A,:,', '))
         ELSE
            WRITE (JPRT,20) (HVPLAB(LSTLAB(I))(1:LNHPLB(LSTLAB(I),1)),
     >                       I=1,ICNT)
   20       FORMAT (T20,15(A,:,', '))
         ENDIF
         ICNT=0
         ICHARS=0
      ENDIF
   30 CONTINUE
C
C     IF THE LOOP EXITS, MAKE SURE THAT REMAINING OUTPUT IS WRITTEN
C
      IF (ICNT.GT.0) THEN
         IPASS=IPASS+1
         IF (IPASS.LE.1) THEN
            WRITE (JPRT,10) (HVPLAB(LSTLAB(I))(1:LNHPLB(LSTLAB(I),1)),
     >                       I=1,ICNT)
         ELSE
            WRITE (JPRT,20) (HVPLAB(LSTLAB(I))(1:LNHPLB(LSTLAB(I),1)),
     >                       I=1,ICNT)
         ENDIF
      ENDIF
   40 CONTINUE
      RETURN
      END
