      SUBROUTINE SPRDIS (CID,A,IRC)
      IMPLICIT NONE
C----------
C  **SPRDIS--PPBASE   DATE OF LAST REVISION:  07/31/08
C----------
C     RETURN THE DISTANCE STAND I IS FROM THE NEAREST ROAD.
C
C     CID = STAND IDENTIFICATION
C     A   = ROAD DISTANCE
C     IRC = RETURN CODE, 0=OK, 1=NO DATA FOR THIS STAND.
C
COMMONS
C
C
      INCLUDE 'PRGPRM.F77'
C
C
      INCLUDE 'PPEPRM.F77'
C
C
      INCLUDE 'PPSPRD.F77'
C
C
COMMONS
C
      INTEGER IRC,IP
      REAL A
C
      CHARACTER*26 CID
      IF (NRDS.GT.0) THEN
         CALL SPBSRX (NRDS,CRDID,IRDORD,CID,IP)
         IF (IP.GT.0) THEN
            A=RDDIST(IRDORD(IP))
            IRC=0
            RETURN
         ENDIF
      ENDIF
      IRC=1
      RETURN
      END
