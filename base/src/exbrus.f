      SUBROUTINE EXBRUS
      IMPLICIT NONE
C----------
C BASE $Id$
C----------
C
C     ENTRY POINTS FOR PROGNOSIS EXTERNAL REFERENCES TO
C     THE WHITE PINE BLISTER RUST MODEL SUBROUTINES WHEN
C     WHITE PINE BLISTER RUST IS NOT INCLUDED.
C
C
C
      LOGICAL LNOTBK(7),LKECHO
      CHARACTER*8 KEYWRD,NOBR
      REAL ARRAY(7),PROB(*),WEIGHT,TIME
      INTEGER IND(*),IND1(*)
      INTEGER I3(6),NCLAS,I1,I2,KEY,IREC,IVAC,I,ITFN
      REAL RDANUW
      INTEGER IDANUW
      CHARACTER*8 CDANUW
      LOGICAL LDANUW
C
      DATA NOBR/'*NO BRUS'/
C
C----------
C  ENTRY BRCMPR   CALLED FROM COMPRS
C----------
      ENTRY BRCMPR (NCLAS,PROB,IND,IND1)
C----------
C  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
C----------
      IDANUW = NCLAS
      RDANUW = PROB(1)
      IDANUW = IND(1)
      IDANUW = IND1(1)
C
      RETURN
C
C----------
C  ENTRY BRDAM   CALLED FROM DAMCDS
C----------
      ENTRY BRDAM (I1, I3)
C----------
C  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
C----------
      IDANUW = I1
      IDANUW = I3(1)
C
      RETURN
C
C----------
C  ENTRY BRESTB   CALLED FROM ESTAB
C----------
      ENTRY BRESTB (TIME, I1, I2)
C----------
C  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
C----------
      RDANUW = TIME
      IDANUW = I1
      IDANUW = I2
C
      RETURN
C
C----------
C  ENTRY BRTREG   CALLED FROM GRADD
C----------
      ENTRY BRTREG
      RETURN
C
C----------
C  ENTRY BRINIT   CALLED FROM INITRE
C----------
      ENTRY BRINIT
      RETURN
C
C----------
C  ENTRY BRIN
C----------
      ENTRY BRIN (KEYWRD,ARRAY,LNOTBK,LKECHO)
C----------
C  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
C----------
      RDANUW = ARRAY(1)
      CDANUW(1:8) = KEYWRD(1:8)
      LDANUW = LNOTBK(1)
      LDANUW = LKECHO
C
      CALL ERRGRO (.TRUE.,11)
      RETURN
C
C----------
C  ENTRY BRSETP   CALLED FROM MAIN
C----------
      ENTRY BRSETP
      RETURN
C
C----------
C  ENTRY BRPR
C----------
      ENTRY BRPR
      RETURN
C
C----------
C  ENTRY BRROUT
C----------
      ENTRY BRROUT
      RETURN
C
C----------
C  ENTRY BRKEY    CALLED FROM OPLIST
C----------
      ENTRY BRKEY (KEY,KEYWRD)
C----------
C  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
C----------
      IDANUW = KEY
C
      KEYWRD=NOBR
      RETURN
C
C----------
C  ENTRY BRTDEL    CALLED FROM TREDEL
C----------
      ENTRY BRTDEL (IVAC,IREC)
C----------
C  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
C----------
      IDANUW = IVAC
      IDANUW = IREC
C
      RETURN
C
C----------
C  ENTRY BRSOR    CALLED FROM TRESOR
C----------
      ENTRY BRSOR
      RETURN
C
C----------
C  ENTRY BRTRIP   CALLED FROM TRIPLE
C----------
      ENTRY BRTRIP (ITFN,I,WEIGHT)
C----------
C  DUMMY ARGUMENT NOT USED WARNING SUPPRESSION SECTION
C----------
      IDANUW = ITFN
      IDANUW = I
      RDANUW = WEIGHT
C
      RETURN
C
      END
